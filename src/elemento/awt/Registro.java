    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection con=null;
        try{
            con=getConnection();
            ps=con.prepareStatement("INSERT INTO usuarios(matricula,nombre,edad,carrera,correo) VALUES(?,?,?,?,?)");
            ps.setString(1,txtMatricula.getText());
            ps.setString(2,txtNombre.getText());
            ps.setString(3,txtEdad.getText());
            ps.setString(4,txtCarrera.getText());
            ps.setString(5,txtCorreo.getText());
            int res=ps.executeUpdate();
            if(res>0){
            JOptionPane.showMessageDialog(null,"usuario guardado con √©xito");
            limpiar();
            }else{
                JOptionPane.showMessageDialog(null,"Error al guardar usuario");
                limpiar();
            }
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"error al insertar usuario");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Connection con=null;
        try{
            con=getConnection();
            ps=con.prepareStatement("SELECT * FROM usuarios WHERE matricula=?");
            ps.setString(1, txtMatricula.getText());
            rs=ps.executeQuery();
            if(rs.next()){
                txtId.setText(rs.getString("id"));
                txtNombre.setText(rs.getString("nombre"));
                txtEdad.setText(rs.getString("edad"));
                txtCarrera.setText(rs.getString("carrera"));
                txtCorreo.setText(rs.getString("correo"));
            }else{
                JOptionPane.showMessageDialog(null, "El usuario no existe");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"error al insertar usuario");
        }    }//GEN-LAST:event_btnBuscarActionPerformed

